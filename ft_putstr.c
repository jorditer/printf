#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	size;

	size = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (*s)
	{
		size += write(1, s, 1);
		s++;
	}
	return (size);
}
